package com.example.myapp.Models;

public class Anouncements {

    String  tittle , How ,when , what , anouncement_id ;

    public Anouncements() {
    }

    public String getTittle() {
        return tittle;
    }

    public void setTittle(String tittle) {
        this.tittle = tittle;
    }

    public String getHow() {
        return How;
    }

    public void setHow(String how) {
        How = how;
    }

    public String getWhen() {
        return when;
    }

    public void setWhen(String when) {
        this.when = when;
    }

    public String getWhat() {
        return what;
    }

    public void setWhat(String what) {
        this.what = what;
    }

    public String getAnouncement_id() {
        return anouncement_id;
    }

    public void setAnouncement_id(String anouncement_id) {
        this.anouncement_id = anouncement_id;
    }
}
